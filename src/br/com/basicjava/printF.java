package br.com.basicjava;

public class printF {
    public static void main(String[] args) {
        boolean myboolean = true;
        char mychar = 'A';
        String mystring = "benevanio";
        int myint = 10;
        double mydouble = 10.0;

        System.out.printf("myboolean = %b \n", myboolean);
        System.out.printf("mychar = %c \n", mychar);
        System.out.printf("mystring = %s \n", mystring);
        System.out.printf("myint = %d \n", myint);
        System.out.printf("mydouble = %f \n", mydouble);
    }
}
